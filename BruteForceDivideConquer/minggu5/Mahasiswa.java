package minggu5;

class Mahasiswa {
    String nama;
    String nim;
    int thnMasuk;
    int uts;
    int uas;

    Mahasiswa (String nama, String nim, int thnMasuk, int uts, int uas) {
        this.nama = nama;
        this.nim = nim;
        this.thnMasuk = thnMasuk;
        this.uts = uts;
        this.uas = uas;
    }
}
